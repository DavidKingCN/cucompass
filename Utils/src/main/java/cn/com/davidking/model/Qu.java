package cn.com.davidking.model;

public class Qu {
	private String quCode;
	private Integer quType;
	private Double quCoin;
	private Integer quNd;
	private String level;
	private String name;
	public String getQuCode() {
		return quCode;
	}
	public void setQuCode(String quCode) {
		this.quCode = quCode;
	}
	public Integer getQuType() {
		return quType;
	}
	public void setQuType(Integer quType) {
		this.quType = quType;
	}
	public Double getQuCoin() {
		return quCoin;
	}
	public void setQuCoin(Double quCoin) {
		this.quCoin = quCoin;
	}
	public Integer getQuNd() {
		return quNd;
	}
	public void setQuNd(Integer quNd) {
		this.quNd = quNd;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
